# Numeric Operators

# Addition
my_num = 4 + 4
print(my_num)
# Returns 8
# If you add a float and a integer, a float will be returned


# Multiplication
my_num = 3 * 3 
print(my_num)
# Returns 9

# Division
my_num = 5 / 3
print(my_num)
# Returns 1.6666666666666667

# Division will always return a float
my_num = 6 / 2
print(my_num)
# Returns 3.0

# Floor Division
# Returns that largest possible integer that evenly divides (does not return a float)
my_num = 5 // 3
print(my_num)
# Returns 1

# To get whole number remainder, use the modular operator
my_num = 5 % 3
print(my_num)
# returns 2
# This is a good way to do an even/odd check
# Even numbers return 0, odd numbers return 1

# Exponents
my_num = 2 ** 3
print(my_num)
# Returns 8 : 2 * 2 * 2