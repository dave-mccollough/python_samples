# Typecasting
# How to convert an object from one type to another

# Integer to Float
# Use the float() function
print(float(1))
# Returns 1.0

# Float to Integer
# Use the int() function
print(int(1.3))
# Returns 1
# Does not round...  removes everything after the decimal point

# Convert integer to string
# Use the str() function
print(str(1.3))
# Returns '1.3'
print(str(False))
# Returns 'False'

# Convert string to integer
# Use the int() function
print(int('7'))
# Returns 7

# Convert string to float
# Use the float() function
print(float('8.7'))
# Returns 8.7

