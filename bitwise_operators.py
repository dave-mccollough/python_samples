print("BitWise Operators")
a = 0b010 #Binary integer
print(a)
# returns 2
print(bin(a))
# returns 0b010
# bin function shows binary representation

# Tilde Bitwise Operator - returns the complement value
print("Bitwise Tilde")
print(~a)
# Returns -3
# Formula: -1 * variable - 1

print("AND OR XOR NOT")
# OR operator requires that either value is true
a = 0b1001
b = 0b1101
# Bitwise OR operator is a single | 
print(bin(a | b))
# returns 0b1101

# AND operator requires both values to be true
a = 0b1001
b = 0b1101
# Bitwise AND operator is a ampersand &
print(bin(a & b))
# returns 0b1001

