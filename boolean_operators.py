# Boolean Operators

# When using the OR operator, if there is a true value, true will be returned
print(True or False)
# Returns True

print(True or True)
# Returns True

print(False or True)
# Returns True

print(False or False)
# Returns False

# The AND operator requires that both values are true
print(True and False)
# Returns False

print(True and True)
# Returns True

print(False and True)
# Returns False

print(False and False)
# Returns False

