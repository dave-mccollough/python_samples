# Numbers

# Adding two integers
my_num = 4 + 4
print(my_num)
# Returns 8

# Adding two floats
my_num = 2.0 + 2.0
print(my_num)
# Returns 4.0

# You can use scientific notation (exponents)
my_num = 4.5e9
print(my_num)
# Returns 4500000000.03
print(4.5e9 == 4.5 * (10 ** 9))
# Returns true

# This works in reverse as well
my_num = 4.5e-2
print(my_num)
# Returns 0.045

