# Literals, Variables and Comments

# Comments

# This is a full line comment

print("Hello World!") #This is a trailing comment

# Python does not support block comments


# Variables
# Start with lowercase letter
# Use 'snake case'
my_str = "This is a simple string"

# Variables can't be keywords

# To print a variable to the screen
print(my_str)

# Variable values can be reassigned
my_str = "This is a different string"

# String Operators
# +=
# Allows you to append to the end of the screen
# Is shorthand for:
my_str = my_str + "testing"

#Example
my_str += " testing"
print(my_str)
# Returns:  "This is a different string testing"

# Variable types in Python are not strict (statically typed)
